    public static boolean isAllBlank(final CharSequence... css) {
        if (ArrayAide.isEmpty(css)) {
            return true;
        }
        for (CharSequence cs : css) {
            if (isNotBlank(cs)) {
                return false;
            }
        }
        return true;
    }

